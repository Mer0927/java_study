package day1;

public class 회원관리 {

	public static void main(String[] args) {
		//회원 이름, 나이
		String[] names = new String[100];
		int[] ages = new int[100];
		int count = 0; //저장회원수
		//회원추가
		names[count] = "홍";
		ages[count] = 30;
		count++; // 1
		
		names[count] = "김";
		ages[count] = 33;
		count++; //2

	}//end main

}//end class
